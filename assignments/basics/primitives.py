def get_int():
    '''
    Returns a variable of type 'int'.
    '''
    my_int = int(5)
    return my_int

def get_float():
    '''
    Returns a variable of type 'float'.
    '''
    my_float = float(5.5)
    return my_float

def get_str():
    ''' 
    Returns a variable of type 'str'.
    '''
    my_str = str("foot")
    return my_str

def get_bool():
    ''' 
    Returns a variable of type 'bool'.
    '''
    my_bool = bool(True)
    return my_bool